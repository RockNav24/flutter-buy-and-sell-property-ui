import 'package:buy_and_sell_property_app/Components/BottomNavbar/bottomNavbarProvider.dart';
import 'package:buy_and_sell_property_app/homePage.dart';
import 'package:flutter/material.dart';
import 'package:provider/provider.dart';

void main() {
  runApp(MyApp());
}

class MyApp extends StatelessWidget {
  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return ChangeNotifierProvider<BottomNavbarProvider>.value(
